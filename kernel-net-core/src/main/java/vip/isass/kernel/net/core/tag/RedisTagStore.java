package vip.isass.kernel.net.core.tag;

import cn.hutool.core.collection.CollUtil;
import cn.hutool.core.util.StrUtil;
import org.springframework.beans.factory.InitializingBean;
import org.springframework.dao.DataAccessException;
import org.springframework.data.redis.core.HashOperations;
import org.springframework.data.redis.core.RedisOperations;
import org.springframework.data.redis.core.RedisTemplate;
import org.springframework.data.redis.core.SessionCallback;
import vip.isass.kernel.net.core.NetRedisKey;

import javax.annotation.Nonnull;
import javax.annotation.Resource;
import java.util.ArrayList;
import java.util.Collection;
import java.util.Iterator;
import java.util.List;
import java.util.Map;
import java.util.Set;
import java.util.stream.Collectors;

public class RedisTagStore implements InitializingBean {

    @Resource
    private RedisTemplate<String, Map<String, Set<String>>> redisTemplate;

    private HashOperations<String, String, Set<String>> hashOperations;

    @Override
    public void addTags(@Nonnull Collection<String> sessionIds, @Nonnull Collection<TagPair> tagPairs) {
        if (sessionIds.isEmpty() || tagPairs.isEmpty()) {
            return;
        }

        if (sessionIds.size() == 1) {
            if (tagPairs.size() == 1) {
                TagPair tag = tagPairs.iterator().next();
                hashOperations.put(NetRedisKey.formatTagKey(null, sessionIds.iterator().next()), tag.getTagKey(), tag.getValues());
            } else {
                hashOperations.putAll(NetRedisKey.formatTagKey(null, sessionIds.iterator().next()), TagUtil.tagPairsToMap(tagPairs));
            }
            return;
        }

        redisTemplate.execute(new SessionCallback<Object>() {
            @Override
            @SuppressWarnings("unchecked")
            public Object execute(@Nonnull RedisOperations operations) throws DataAccessException {
                operations.multi();
                HashOperations<String, String, Set<String>> ops = operations.opsForHash();
                if (tagPairs.size() == 1) {
                    TagPair next = tagPairs.iterator().next();
                    for (String sessionId : sessionIds) {
                        ops.put(NetRedisKey.formatTagKey(null, sessionId), next.getTagKey(), next.getValues());
                    }
                } else {
                    Map<String, Set<String>> map = TagUtil.tagPairsToMap(tagPairs);
                    for (String sessionId : sessionIds) {
                        ops.putAll(NetRedisKey.formatTagKey(null, sessionId), map);
                    }
                }
                return operations.exec();
            }
        });
    }

    @Override
    public Map<String, Set<String>> findTags(@Nonnull String sessionId) {
        return hashOperations.entries(NetRedisKey.formatTagKey(null, sessionId));
    }

    @Override
    public Set<String> findTagValues(@Nonnull String sessionId, @Nonnull String tagKey) {
        return hashOperations.get(NetRedisKey.formatTagKey(null, sessionId), tagKey);
    }

    @Override
    public String getTagValue(@Nonnull String sessionId, @Nonnull String tagKey) {
        Set<String> tagValues = hashOperations.get(NetRedisKey.formatTagKey(null, sessionId), tagKey);
        return CollUtil.getFirst(tagValues);
    }

    @Override
    public boolean containAllTags(@Nonnull String sessionId, @Nonnull Collection<TagPair> tagPairs) {
        if (StrUtil.isBlank(sessionId)) {
            return false;
        }
        if (CollUtil.isEmpty(tagPairs)) {
            return true;
        }

        int batchSize = 10;
        String key = NetRedisKey.formatTagKey(null, sessionId);
        if (tagPairs.size() <= batchSize) {
            return doContainAllTags(key, tagPairs);
        }
        List<List<TagPair>> split = CollUtil.split(tagPairs, batchSize);
        for (List<TagPair> pairs : split) {
            if (doContainAllTags(key, pairs)) {
                return true;
            }
        }
        return false;
    }

    private boolean doContainAllTags(String redisKey, Collection<TagPair> shouldHaveTags) {
        List<Set<String>> existingTagValues = hashOperations.multiGet(
                redisKey,
                shouldHaveTags.stream().map(TagPair::getTagKey).collect(Collectors.toList()));
        Iterator<TagPair> iterator = shouldHaveTags.iterator();
        int i = -1;
        while (iterator.hasNext()) {
            i++;
            TagPair shouldHaveTagPair = iterator.next();
            Set<String> existingTagValueSet = existingTagValues.get(i);

            // 如果没有实际标签值，则肯定不符合条件
            if (existingTagValueSet == null) {
                return false;
            }

            // 如果需要拥有的标签值是任意值，则此标签键值对符合条件
            if (shouldHaveTagPair.isEmptyValues()) {
                continue;
            }

            Set<String> shouldHavTagValues = shouldHaveTagPair.getValues();

            // 如果需要拥有的标签值的数量大于实际拥有的标签值数量，则肯定不符合条件
            if (shouldHavTagValues.size() > existingTagValueSet.size()) {
                return false;
            }

            // 如果实际存在的标签值没有包含全部需要拥有的标签值，则肯定不符合条件
            if (!existingTagValueSet.containsAll(shouldHavTagValues)) {
                return false;
            }
        }
        return true;
    }

    @Override
    public boolean containAnyTag(@Nonnull String sessionId, @Nonnull Collection<TagPair> tagPairs) {
        if (tagPairs.isEmpty()) {
            return false;
        }
        int batchSize = 10;
        String key = NetRedisKey.formatTagKey(null, sessionId);
        if (tagPairs.size() <= batchSize) {
            return doHasAnyTagPair(key, tagPairs);
        }
        List<List<TagPair>> split = CollUtil.split(tagPairs, batchSize);
        for (List<TagPair> pairs : split) {
            if (doHasAnyTagPair(key, pairs)) {
                return true;
            }
        }

        return false;
    }

    private boolean doHasAnyTagPair(String redisKey, Collection<TagPair> tagPairs) {
        List<Set<String>> existingTagValues = hashOperations.multiGet(
                redisKey,
                tagPairs.stream().map(TagPair::getTagKey).collect(Collectors.toList()));
        Iterator<TagPair> iterator = tagPairs.iterator();
        int i = -1;
        while (iterator.hasNext()) {
            i++;
            TagPair shouldHaveTagPair = iterator.next();
            Set<String> existingTagValueSet = existingTagValues.get(i);

            // 如果没有实际标签值，则肯定不符合条件
            if (existingTagValueSet == null) {
                continue;
            }

            Set<String> shouldHavTagValues = shouldHaveTagPair.getValues();

            // 如果需要拥有的标签值是任意值，则肯定符合条件
            if (shouldHaveTagPair.isEmptyValues()) {
                return true;
            }

            // 如果实际存在的标签值包含了所有需要拥有的标签值，则肯定符合条件
            if (CollUtil.containsAny(existingTagValueSet, shouldHavTagValues)) {
                return true;
            }
        }
        return false;
    }

    @Override
    public void removeTags(@Nonnull Collection<String> sessionIds) {
        if (sessionIds.isEmpty()) {
            return;
        }
        redisTemplate.delete(sessionIds);
    }

    @Override
    public void removeTags(@Nonnull Collection<String> sessionIds, @Nonnull Collection<TagPair> tagPairs) {
        for (String sessionId : sessionIds) {
            String redisKey = NetRedisKey.formatTagKey(null, sessionId);
            List<Set<String>> existingTags = hashOperations.multiGet(
                    redisKey,
                    tagPairs.stream()
                            .map(TagPair::getTagKey)
                            .collect(Collectors.toList()));
            List<Object> willDeleteTagKeys = new ArrayList<>();
            Iterator<TagPair> iterator = tagPairs.iterator();
            int i = -1;
            while (iterator.hasNext()) {
                i++;
                TagPair shouldHaveTagPair = iterator.next();
                Set<String> existingTagValueSet = existingTags.get(i);
                if (existingTagValueSet == null) {
                    continue;
                }

                if (shouldHaveTagPair.isEmptyValues()) {
                    willDeleteTagKeys.add(shouldHaveTagPair.getTagKey());
                    continue;
                }

                if (existingTagValueSet.containsAll(shouldHaveTagPair.getValues())) {
                    willDeleteTagKeys.add(shouldHaveTagPair.getTagKey());
                }
            }

            if (CollUtil.isNotEmpty(willDeleteTagKeys)) {
                hashOperations.delete(redisKey, willDeleteTagKeys.toArray());
            }
        }
    }

    @Override
    public void removeTagKeys(@Nonnull Collection<String> sessionIds, @Nonnull Collection<String> tagKeys) {
        if (sessionIds.isEmpty() || tagKeys.isEmpty()) {
            return;
        }

        if (sessionIds.size() == 1) {
            hashOperations.delete(
                    NetRedisKey.formatTagKey(null, sessionIds.iterator().next()),
                    tagKeys.toArray());
        }
        redisTemplate.execute(new SessionCallback<Object>() {
            @Override
            @SuppressWarnings("unchecked")
            public Object execute(@Nonnull RedisOperations operations) throws DataAccessException {
                operations.multi();
                HashOperations<String, String, Set<String>> ops = operations.opsForHash();
                Object[] keyArr = tagKeys.toArray();
                for (String sessionId : sessionIds) {
                    ops.delete(NetRedisKey.formatTagKey(null, sessionId), keyArr);
                }
                return operations.exec();
            }
        });
    }

    @Override
    public void afterPropertiesSet() {
        hashOperations = redisTemplate.opsForHash();
    }
}
