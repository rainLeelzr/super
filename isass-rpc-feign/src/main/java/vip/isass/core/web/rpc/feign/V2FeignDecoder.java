package vip.isass.core.web.rpc.feign;

import cn.hutool.core.collection.CollUtil;
import cn.hutool.core.io.IoUtil;
import cn.hutool.core.util.StrUtil;
import cn.hutool.json.JSONUtil;
import com.fasterxml.jackson.core.type.TypeReference;
import com.fasterxml.jackson.databind.JavaType;
import com.fasterxml.jackson.databind.JsonNode;
import feign.FeignException;
import feign.Response;
import feign.codec.Decoder;
import lombok.extern.slf4j.Slf4j;
import org.springframework.core.ResolvableType;
import org.springframework.http.HttpHeaders;
import org.springframework.http.HttpInputMessage;
import org.springframework.http.HttpStatus;
import org.springframework.http.MediaType;
import org.springframework.http.ResponseEntity;
import org.springframework.http.client.ClientHttpResponse;
import org.springframework.http.converter.GenericHttpMessageConverter;
import org.springframework.http.converter.HttpMessageConverter;
import org.springframework.http.converter.HttpMessageNotReadableException;
import org.springframework.lang.Nullable;
import org.springframework.util.Assert;
import org.springframework.util.StreamUtils;
import org.springframework.web.client.RestClientException;
import vip.isass.core.serialization.jackson.JsonUtil;
import vip.isass.core.web.Resp;

import java.io.IOException;
import java.io.InputStream;
import java.lang.reflect.Type;
import java.nio.charset.Charset;
import java.nio.charset.StandardCharsets;
import java.util.ArrayList;
import java.util.Collection;
import java.util.List;
import java.util.Map;

/**
 * isass v2 接口 feign 响应解码
 * 解析响应内容的 Resp 部分，抽取 body 字段返回。resp.success = false 时，尽量还原（抛出）目标微服务所产生的异常
 */
@Slf4j
public class V2FeignDecoder implements Decoder {

    private final List<HttpMessageConverter<?>> messageConverters;

    private final Charset defaultCharset;

    private static final TypeReference<Resp<JsonNode>> RESP_TYPE_REFERENCE = new TypeReference<Resp<JsonNode>>() {
    };

    public V2FeignDecoder(List<HttpMessageConverter<?>> messageConverters) {
        this(messageConverters, StandardCharsets.UTF_8);
    }

    public V2FeignDecoder(List<HttpMessageConverter<?>> messageConverters, Charset defaultCharset) {
        Assert.notNull(defaultCharset, "Default Charset must not be null");
        this.defaultCharset = defaultCharset;
        this.messageConverters = messageConverters;
    }

    @Override
    public Object decode(Response response, Type type) throws IOException, FeignException {
        if (type instanceof Resp
                || type instanceof ResponseEntity
                || !IV2FeignService.class.isAssignableFrom(response.request().requestTemplate().feignTarget().type())) {
            return decodeByHttpMessageConverters(response, type);
        }

        // 如果返回内容含有 Resp 结构，则需要抽取 body 内容返回
        InputStream inputStream = response.body().asInputStream();
        HttpHeaders httpHeaders = getHttpHeaders(response.headers());
        MediaType contentType = httpHeaders.getContentType();
        Charset charset = getContentTypeCharset(contentType);
        String responseStr = StreamUtils.copyToString(inputStream, charset);

        if (!JSONUtil.isJson(responseStr)) {
            return decodeByHttpMessageConverters(response, type, responseStr, charset, httpHeaders);
        }

        Resp<JsonNode> resp = JsonUtil.DEFAULT_INSTANCE.readValue(responseStr, RESP_TYPE_REFERENCE);
        if (resp.getSuccess() == null
                || resp.getMessage() == null) {
            return decodeByHttpMessageConverters(response, type, responseStr, charset, httpHeaders);
        }

        resp.exceptionIfUnSuccess();

        return decodeByUnwrapResp(type, resp.getData());
    }

    private Object decodeByUnwrapResp(Type type, JsonNode dataJsonNode) {
        if (dataJsonNode == null || dataJsonNode.isNull()) {
            return null;
        }

        JavaType javaType = JsonUtil.DEFAULT_INSTANCE.getTypeFactory().constructType(type);
        return JsonUtil.NOT_NULL_INSTANCE.convertValue(dataJsonNode, javaType);
    }

    @SuppressWarnings({"unchecked", "rawtypes"})
    private Object decodeByHttpMessageConverters(Response response, Type type, HttpInputMessage inputMessage) {
        MediaType contentType = getContentType(response);
        Class<?> responseClass = (type instanceof Class ? (Class<?>) type : null);
        try {
            for (HttpMessageConverter<?> messageConverter : this.messageConverters) {
                if (messageConverter instanceof GenericHttpMessageConverter) {
                    GenericHttpMessageConverter<?> genericMessageConverter =
                            (GenericHttpMessageConverter<?>) messageConverter;
                    if (genericMessageConverter.canRead(type, null, contentType)) {
                        ResolvableType resolvableType = ResolvableType.forType(type);
                        log.trace("Reading to [{}]", resolvableType);
                        return genericMessageConverter.read(type, null, inputMessage);
                    }
                }
                if (responseClass != null) {
                    if (messageConverter.canRead(responseClass, contentType)) {
                        log.trace("Reading to [{}] as \"{}\"", responseClass.getName(), contentType);
                        return messageConverter.read((Class) responseClass, inputMessage);
                    }
                }
            }
        } catch (IOException | HttpMessageNotReadableException ex) {
            throw new RestClientException(
                    StrUtil.format(
                            "Error while extracting response for type [{}] and content type [{}]",
                            type,
                            contentType),
                    ex);
        }

        throw new RestClientException(
                StrUtil.format(
                        "Could not extract response: no suitable HttpMessageConverter found " +
                                "for response type [{}] and content type [{}]",
                        type,
                        contentType));
    }

    private Object decodeByHttpMessageConverters(Response response, Type type) {
        return decodeByHttpMessageConverters(response, type, new FeignResponseAdapter(response));
    }

    private Object decodeByHttpMessageConverters(Response response,
                                                 Type type,
                                                 String responseStr,
                                                 Charset charset,
                                                 HttpHeaders httpHeaders) {
        return decodeByHttpMessageConverters(response, type, new HttpInputMessage() {
            @Override
            public InputStream getBody() {
                return IoUtil.toStream(responseStr, charset);
            }

            @Override
            public HttpHeaders getHeaders() {
                return httpHeaders;
            }
        });
    }

    protected MediaType getContentType(Response response) {
        Collection<String> contentTypes = response.headers().get(HttpHeaders.CONTENT_TYPE);
        if (CollUtil.isEmpty(contentTypes)) {
            return MediaType.APPLICATION_OCTET_STREAM;
        }
        try {
            return contentTypes.stream()
                    .filter(StrUtil::isNotBlank)
                    .map(MediaType::parseMediaType)
                    .findFirst()
                    .orElse(MediaType.APPLICATION_OCTET_STREAM);
        } catch (Exception e) {
            log.warn("解析feign[{}]的contentType错误，将使用 'application/octet-stream'", response.request().toString());
            return MediaType.APPLICATION_OCTET_STREAM;
        }
    }

    static HttpHeaders getHttpHeaders(Map<String, Collection<String>> headers) {
        HttpHeaders httpHeaders = new HttpHeaders();
        for (Map.Entry<String, Collection<String>> entry : headers.entrySet()) {
            httpHeaders.put(entry.getKey(), new ArrayList<>(entry.getValue()));
        }
        return httpHeaders;
    }

    private static final class FeignResponseAdapter implements ClientHttpResponse {

        private final Response response;

        private FeignResponseAdapter(Response response) {
            this.response = response;
        }

        @Override
        public HttpStatus getStatusCode() {
            return HttpStatus.valueOf(this.response.status());
        }

        @Override
        public int getRawStatusCode() {
            return this.response.status();
        }

        @Override
        public String getStatusText() {
            return this.response.reason();
        }

        @Override
        public void close() {
            try {
                this.response.body().close();
            } catch (IOException ex) {
                // Ignore exception on close...
            }
        }

        @Override
        public InputStream getBody() throws IOException {
            return this.response.body().asInputStream();
        }

        @Override
        public HttpHeaders getHeaders() {
            return getHttpHeaders(this.response.headers());
        }

    }

    private Charset getContentTypeCharset(@Nullable MediaType contentType) {
        if (contentType != null && contentType.getCharset() != null) {
            return contentType.getCharset();
        } else if (contentType != null && contentType.isCompatibleWith(MediaType.APPLICATION_JSON)) {
            return StandardCharsets.UTF_8;
        } else {
            Charset charset = defaultCharset;
            Assert.state(charset != null, "No default charset");
            return charset;
        }
    }

}
