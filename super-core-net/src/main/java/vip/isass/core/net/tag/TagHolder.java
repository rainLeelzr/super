package vip.isass.core.net.tag;

import cn.hutool.core.collection.CollUtil;
import cn.hutool.core.lang.Assert;
import lombok.Builder;
import lombok.Getter;
import vip.isass.core.net.session.Session;

import java.util.Collection;
import java.util.Collections;
import java.util.HashSet;
import java.util.Iterator;
import java.util.Map;
import java.util.Set;
import java.util.concurrent.ConcurrentHashMap;
import java.util.concurrent.CopyOnWriteArraySet;
import java.util.function.Consumer;
import java.util.stream.Collectors;

/**
 * 标签管理器实现
 * 名称定义：
 * tagKey: 标签键
 * tagValue: 标签值
 * tagPair = tagKey + tagValue: 标签键值对，方便业务调用方使用组合键值对的场景
 * <p>
 * 同一 session 的操作，在高并发时是否出现数据错乱？对 sessionId 使用虚拟节点一致性哈希算法，分配到线程池中处理。
 * 但是上层调用了操作方法，就立即返回了，此时可能未真正操作了标签数据，导致紧接下来的业务会不准确
 *
 * @author Rain
 */
public class TagHolder {

    /**
     * 已登录用户使用此 tagKey 保存 session
     */
    public static final String USER_ID_TAG_KEY = "uid";

    /**
     * <p>{@literal  Map<tagKey, Map<tagValue, Set<Session>>>}
     */
    @Getter
    @Builder.Default
    private final Map<String, Map<String, Set<Session<?>>>> tagValueAndSessionMapByTagKey = new ConcurrentHashMap<>();

    /**
     * <p> {@literal Map<sessionId, Map<tagKey, tagValue>>}
     */
    @Getter
    @Builder.Default
    private final Map<String, Map<String, String>> tagKeyAndTagValueMapBySessionId = new ConcurrentHashMap<>();

    // region add

    /**
     * 给会话添加标签键
     *
     * @param session 会话
     * @param tagKey  标签键
     */
    public void addTagKey(Session<?> session, String tagKey) {
        addTagPairs(
            Collections.singleton(session),
            Collections.singleton(new TagPair(tagKey)));
    }

    /**
     * 给会话添加多个标签键
     *
     * @param session 会话
     * @param tagKeys 标签键集合
     */
    public void addTagKeys(Session<?> session, Collection<String> tagKeys) {
        addTagPairs(
            Collections.singleton(session),
            tagKeys.stream()
                .map(TagPair::new)
                .collect(Collectors.toList()));
    }

    /**
     * 给会话添加标签键值对
     *
     * @param session  会话
     * @param tagKey   标签键
     * @param tagValue 标签值
     */
    public void addTagPair(Session<?> session, String tagKey, String tagValue) {
        addTagPairs(
            Collections.singleton(session),
            Collections.singleton(new TagPair(tagKey, tagValue)));
    }

    /**
     * 给会话添加多个标签键值对
     *
     * @param session  会话
     * @param tagPairs 标签键值对集合
     */
    public void addTagPairs(Session<?> session, Collection<TagPair> tagPairs) {
        addTagPairs(
            Collections.singleton(session),
            tagPairs);
    }

    /**
     * 给多个会话添加多个标签键值对
     *
     * @param sessions 会话集合
     * @param tagPairs 标签键值对集合
     */
    public void addTagPairs(Collection<Session<?>> sessions, Collection<TagPair> tagPairs) {
        for (Session<?> session : sessions) {
            Map<String, String> tagValueMap = tagKeyAndTagValueMapBySessionId.computeIfAbsent(
                session.getSessionId(),
                s -> new ConcurrentHashMap<>(16));

            tagPairs.forEach(tagPair -> tagValueMap.put(
                tagPair.getKey(),
                tagPair.getValue())
            );
        }

        for (TagPair tagPair : tagPairs) {
            Map<String, Set<Session<?>>> tagValueSessionMap = tagValueAndSessionMapByTagKey
                .computeIfAbsent(
                    tagPair.getKey(),
                    t -> new ConcurrentHashMap<>(16));
            Set<Session<?>> sessionSet = tagValueSessionMap.computeIfAbsent(tagPair.getValue(), t -> new CopyOnWriteArraySet<>());
            sessionSet.addAll(sessions);
        }
    }

    // endregion

    // region has

    /**
     * 判断会话是否拥有标签键
     *
     * @param session 会话
     * @param tagKey  标签键
     * @return 会话是否拥有标签键
     */
    public boolean hasTagKey(Session<?> session, String tagKey) {
        Map<String, String> tagPairMap = tagKeyAndTagValueMapBySessionId.get(session.getSessionId());
        return tagPairMap != null && tagPairMap.containsKey(tagKey);
    }

    /**
     * 判断会话是否拥有所有标签键
     *
     * @param session 会话
     * @param tagKeys 标签键集合
     * @return 会话是否拥有所有标签键
     */
    public boolean hasAllTagKey(Session<?> session, Collection<String> tagKeys) {
        Map<String, String> tagPairMap = tagKeyAndTagValueMapBySessionId.get(session.getSessionId());
        if (tagPairMap == null) {
            return false;
        }

        for (String tagKey : tagKeys) {
            if (tagPairMap.get(tagKey) == null) {
                return false;
            }
        }
        return true;
    }

    /**
     * 判断会话是否拥有标签键值对
     *
     * @param session  会话
     * @param tagKey   标签键
     * @param tagValue 标签值
     * @return 会话是否拥有标签键值对
     */
    public boolean hasTagPair(Session<?> session, String tagKey, String tagValue) {
        Map<String, String> tagPairMap = tagKeyAndTagValueMapBySessionId.get(session.getSessionId());
        if (tagPairMap == null) {
            return false;
        }

        return tagValue == null
            ? tagPairMap.containsKey(tagKey)
            : tagValue.equals(tagPairMap.get(tagKey));
    }

    /**
     * 判断会话是否拥有所有标签键值对
     *
     * @param session  会话
     * @param tagPairs 标签键值对
     * @return 会话是否拥有所有标签键值对
     */
    public boolean hasAllTagPair(Session<?> session, Collection<TagPair> tagPairs) {
        Map<String, String> tagPairMap = tagKeyAndTagValueMapBySessionId.get(session.getSessionId());
        if (tagPairMap == null) {
            return false;
        }

        for (TagPair tagPair : tagPairs) {
            String tagValue = tagPairMap.get(tagPair.getKey());
            if (tagValue == null) {
                return false;
            }
            if (tagPair.getValue().isEmpty()) {
                continue;
            }
            if (!tagPair.getValue().equals(tagValue)) {
                return false;
            }
        }
        return true;
    }

    /**
     * 查找拥有所有标签键值对的会话
     * 先查找符合标签键值对集合的第一个标签键值对的会话集合
     * 再从会话集合中剔除不符合余下标签键值对的会话
     *
     * @param tagPairs 标签键值对集合
     * @return 符合条件的会话集合
     */
    public Set<Session<?>> findAllMatchSessionsByTagPairs(Collection<TagPair> tagPairs) {
        if (CollUtil.isEmpty(tagPairs)) {
            return Collections.emptySet();
        }

        TagPair firstTag = tagPairs.iterator().next();
        Assert.notBlank(firstTag.getKey(), "tagKey 不能为空");

        Map<String, Set<Session<?>>> sessionMapByTagValue = tagValueAndSessionMapByTagKey.get(firstTag.getKey());
        if (sessionMapByTagValue == null) {
            return Collections.emptySet();
        }

        Set<Session<?>> sessions = firstTag.getValue() == null
            ? sessionMapByTagValue
            .entrySet()
            .parallelStream()
            .map(Map.Entry::getValue)
            .flatMap(Collection::stream)
            .collect(Collectors.toSet())
            : sessionMapByTagValue.get(firstTag.getValue());

        if (CollUtil.isEmpty(sessions)) {
            return Collections.emptySet();
        }

        Iterator<TagPair> iterator = tagPairs.iterator();
        int i = -1;
        while (iterator.hasNext()) {
            i++;
            TagPair nextTag = iterator.next();
            if (i == 0) {
                continue;
            }

            Assert.notBlank(nextTag.getKey(), "tagKey 不能为空");

            sessions.removeIf(session -> !hasTagPair(session, nextTag.getKey(), nextTag.getValue()));
        }
        return sessions;
    }

    public void consumeAllMatchSessionsByTagPairs(Collection<TagPair> tagPairs, Consumer<Session<?>> consumer) {
        TagPair firstTagPair = CollUtil.getFirst(tagPairs);
        if (firstTagPair == null) {
            return;
        }

        Map<String, Set<Session<?>>> tagValueAndSessionsMap = tagValueAndSessionMapByTagKey.get(firstTagPair.getKey());
        if (tagValueAndSessionsMap == null) {
            return;
        }

        Collection<Set<Session<?>>> sessionCollections = firstTagPair.getValue().isEmpty()
            ? tagValueAndSessionsMap.values()
            : Collections.singleton(tagValueAndSessionsMap.get(firstTagPair.getValue()));

        HashSet<String> sentSessions = new HashSet<>();
        for (Set<Session<?>> sessionSet : sessionCollections) {
            // tagValueAndSessionsMap.get(firstTagPair.getValue()) 有可能返回 null, 需要非空判断
            if (sessionSet == null) {
                continue;
            }

            // 找到 sessionSet 中符合余下标签键值对的会话，执行 consumer
            for (Session<?> session : sessionSet) {
                if (sentSessions.contains(session.getSessionId())) {
                    continue;
                }
                if (tagPairs.size() > 1 && !hasAllTagPair(session, tagPairs)) {
                    continue;
                }
                consumer.accept(session);
                sentSessions.add(session.getSessionId());
            }
        }
    }

    // endregion

    // region remove

    /**
     * 删除会话的标签键
     *
     * @param session 会话
     * @param tagKey  标签键
     */
    public void removeByTagKey(Session<?> session, String tagKey) {
        removeTagPairs(Collections.singleton(session), Collections.singleton(new TagPair(tagKey)));
    }

    /**
     * 删除会话的标签键值对
     *
     * @param session  会话
     * @param tagKey   标签键
     * @param tagValue 标签值
     */
    public void removeByTagPair(Session<?> session, String tagKey, String tagValue) {
        removeTagPairs(Collections.singleton(session), Collections.singleton(new TagPair(tagKey, tagValue)));
    }

    /**
     * 删除多个会话的标签键
     *
     * @param sessions 会话集合
     * @param tagKey   标签键
     */
    public void removeBySessionsAndTagKey(Collection<Session<?>> sessions, String tagKey) {
        removeTagPairs(sessions, Collections.singleton(new TagPair(tagKey)));
    }

    /**
     * 删除多个会话的多个标签键
     *
     * @param sessions 会话集合
     * @param tagKeys  标签键集合
     */
    public void removeTagBySessionsAndTagKeys(Collection<Session<?>> sessions, Collection<String> tagKeys) {
        removeTagPairs(
            sessions,
            tagKeys.stream().map(TagPair::new).collect(Collectors.toList())
        );
    }

    /**
     * 删除多个会话的多个标签键值对
     *
     * @param sessions 会话集合
     * @param tagPairs 标签键值对集合
     */
    public void removeTagPairs(Collection<Session<?>> sessions, Collection<TagPair> tagPairs) {
        for (Session<?> session : sessions) {
            Map<String, String> tagPairMap = tagKeyAndTagValueMapBySessionId.get(session.getSessionId());
            if (tagPairMap == null) {
                // 如果 tagKeyAndTagValueMapBySessionId 没有记录 session 的标签，
                // 那么 tagValueAndSessionMapByTagKey 也一定没被记录
                continue;
            }

            for (TagPair tagPair : tagPairs) {
                String realTagValue = tagPairMap.get(tagPair.getKey());
                if (realTagValue == null) {
                    continue;
                }
                if (!tagPair.getValue().isEmpty() && !tagPair.getValue().equals(realTagValue)) {
                    continue;
                }

                tagPairMap.remove(tagPair.getKey());

                // 因为上面已知 realTagValue != null ，所以这里也一定 tagValueAndSessionMap 和 sessionSet != null
                Map<String, Set<Session<?>>> tagValueAndSessionMap = tagValueAndSessionMapByTagKey.get(tagPair.getKey());
                Set<Session<?>> sessionSet = tagValueAndSessionMap.get(realTagValue);
                sessionSet.remove(session);
                if (sessionSet.isEmpty()) {
                    tagValueAndSessionMap.remove(realTagValue);
                }
                if (tagValueAndSessionMap.isEmpty()) {
                    tagValueAndSessionMapByTagKey.remove(tagPair.getKey());
                }
            }

            // 如果session
            if (tagPairMap.isEmpty()) {
                tagKeyAndTagValueMapBySessionId.remove(session.getSessionId());
            }
        }
    }

    /**
     * 删除会话的所有标签
     *
     * @param session 会话
     */
    public void removeAllTags(Session<?> session) {
        removeAllTags(Collections.singleton(session));
    }

    /**
     * 删除多个会话的所有标签
     *
     * @param sessions 会话集合
     */
    public void removeAllTags(Collection<Session<?>> sessions) {
        sessions.forEach(session -> {
            Map<String, String> removedTagPairMap = tagKeyAndTagValueMapBySessionId.remove(session.getSessionId());
            if (removedTagPairMap == null) {
                return;
            }

            for (Map.Entry<String, String> tagPairEntry : removedTagPairMap.entrySet()) {
                Map<String, Set<Session<?>>> tagValueAndSessionsMap = tagValueAndSessionMapByTagKey.get(tagPairEntry.getKey());
                Set<Session<?>> sessionSet = tagValueAndSessionsMap.get(tagPairEntry.getValue());
                sessionSet.remove(session);

                if (sessionSet.isEmpty()) {
                    tagValueAndSessionsMap.remove(tagPairEntry.getValue());
                }
                if (tagValueAndSessionsMap.isEmpty()) {
                    tagValueAndSessionMapByTagKey.remove(tagPairEntry.getKey());
                }
            }
        });
    }

    // endregion

}
