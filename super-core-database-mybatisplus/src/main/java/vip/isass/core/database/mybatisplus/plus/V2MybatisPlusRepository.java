import java.util.*;
import java.util.stream.Collectors;

/**
 * @author Rain
 */
@Slf4j
public abstract class V2MybatisPlusRepository<
    E extends IV2Entity<E>,
    EDB extends IV2DbEntity<E, EDB>,
    C extends IV2Criteria<E, C>,
    M extends IMapper<EDB>
    >
    extends ServiceImpl<M, EDB>
    implements IV2Repository<E, C> {

    // ****************************** 增 start ******************************
    @Override
    public boolean add(E entity) {
        EDB edb = V2DbEntityConvert.convertToDbEntity(entity);
        super.save(edb);
        // 保存到数据库后，自动赋值的字段，被赋值在 edb 中，需要复制到 entity 中
        if (entity instanceof IV2IdEntity) {
            ((IV2IdEntity) entity).setId(((IV2IdEntity) edb).getId());
        }
        if (entity instanceof IV2TraceEntity) {
            IV2TraceEntity traceEntity = (IV2TraceEntity) entity;
            IV2TraceEntity traceDbEntity = (IV2TraceEntity) edb;
            traceEntity.setCreateTime(traceDbEntity.getCreateTime());
            traceEntity.setCreateUserId(traceDbEntity.getCreateUserId());
            traceEntity.setCreateUserName(traceDbEntity.getCreateUserName());
            traceEntity.setModifyTime(traceDbEntity.getModifyTime());
            traceEntity.setModifyUserId(traceDbEntity.getModifyUserId());
            traceEntity.setModifyUserName(traceDbEntity.getModifyUserName());
        }
        if (entity instanceof IV2ParentIdEntity) {
            IV2ParentIdEntity parentIdEntity = (IV2ParentIdEntity) entity;
            IV2ParentIdEntity parentIdDbEntity = (IV2ParentIdEntity) edb;
            parentIdEntity.setParentId(parentIdDbEntity.getParentId());
        }
        if (entity instanceof IV2LogicDeleteEntity) {
            IV2LogicDeleteEntity logicDeleteEntity = (IV2LogicDeleteEntity) entity;
            IV2LogicDeleteEntity logicDeleteDbEntity = (IV2LogicDeleteEntity) edb;
            logicDeleteEntity.setDeleteFlag(logicDeleteDbEntity.getDeleteFlag());
        }
        if (entity instanceof IV2VersionEntity) {
            IV2VersionEntity versionEntity = (IV2VersionEntity) entity;
            IV2VersionEntity versionDbEntity = (IV2VersionEntity) edb;
            versionEntity.setVersion(versionDbEntity.getVersion());
        }
        if (entity instanceof IV2TenantEntity) {
            IV2TenantEntity tenantEntity = (IV2TenantEntity) entity;
            IV2TenantEntity tenantDbEntity = (IV2TenantEntity) edb;
            tenantEntity.setTenantId(tenantDbEntity.getTenantId());
        }

        return true;
    }

    @Override
    public boolean addBatch(Collection<E> entities) {
        if (CollUtil.isEmpty(entities)) {
            return false;
        }
        return super.saveBatch(V2DbEntityConvert.convertToEdbEntities(entities));
    }

    @Override
    public boolean addBatch(Collection<E> entities, int batchSize) {
        return super.saveBatch(V2DbEntityConvert.convertToEdbEntities(entities), batchSize);
    }

    @Override
    public boolean addIfAbsentByCriteria(E entity, C criteria) {
        if (isPresentByCriteria(criteria)) {
            return false;
        }
        add(entity);
        return true;
    }

    @Override
    public E addOrUpdate(E entity, List<String> uniqueColumns) {
        Assert.notEmpty(uniqueColumns, "uniqueColumns");
        QueryWrapper<EDB> wrapper = new QueryWrapper<>();
        Map<String, Object> map = BeanUtil.beanToMap(entity);
        for (String uniqueColumn : uniqueColumns) {
            Object value = map.get(StrUtil.toCamelCase(uniqueColumn));
            Assert.notNull(value, "uniqueColumn[{}]必填", uniqueColumn);
            wrapper.eq(uniqueColumn, value);
        }

        if (isPresentByWrapper(wrapper)) {
            updateByWrapper(entity, wrapper);
        } else {
            add(entity);
        }
        return entity;
    }

    public boolean addIfAbsentByColumns(E entity, List<String> uniqueColumns) {
        Assert.notEmpty(uniqueColumns, "uniqueColumns");
        QueryWrapper<EDB> wrapper = new QueryWrapper<>();
        Map<String, Object> map = BeanUtil.beanToMap(entity);
        for (String uniqueColumn : uniqueColumns) {
            Object value = map.get(StrUtil.toCamelCase(uniqueColumn));
            Assert.notNull(value, "uniqueColumn[{}]必填", uniqueColumn);
            wrapper.eq(uniqueColumn, value);
        }

        if (isPresentByWrapper(wrapper)) {
            return false;
        }
        add(entity);
        return true;
    }

    // ****************************** 删 start ******************************

    @Override
    public boolean deleteById(Serializable id) {
        return super.removeById(id);
    }

    @Override
    public boolean deleteByIds(Collection<? extends Serializable> ids) {
        return super.removeByIds(ids);
    }

    public boolean deleteByWrapper(Wrapper<EDB> wrapper) {
        Assert.isTrue(!wrapper.isEmptyOfNormal(), "删除失败，删除条件不能为空");
        return super.remove(wrapper);
    }

    @Override
    public boolean deleteByCriteria(IV2Criteria<E, C> criteria) {
        return this.deleteByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    //****************************** 改 start ******************************

    @Override
    @SuppressWarnings("rawtypes")
    public boolean updateById(E entity) {
        IV2IdEntity idEntity = (IV2IdEntity) entity;
        Serializable id = idEntity.getId();
        Assert.notNull(id, "id 不能为null");
        if (id instanceof String) {
            Assert.notBlank((String) id, "id 不能为空");
        }
        EDB edb = V2DbEntityConvert.convertToDbEntity(entity);

        String idColumnName = idEntity.getIdColumnName();
        boolean b = IV2IdEntity.ID_COLUMN_NAME.equalsIgnoreCase(idColumnName)
            ? super.updateById(edb)
            : super.update(edb, new UpdateWrapper<EDB>().eq(idColumnName, idEntity.getId()));

        // 回写版本字段到 entity
        if (entity instanceof IV2VersionEntity) {
            IV2VersionEntity versionEntity = (IV2VersionEntity) entity;
            IV2VersionEntity versionDbEntity = (IV2VersionEntity) edb;
            versionEntity.setVersion(versionDbEntity.getVersion());
        }
        return b;
    }

    @Override
    @SuppressWarnings("rawtypes")
    public boolean updateAllColumnsById(E entity) {
        IV2IdEntity idEntity = (IV2IdEntity) entity;
        Serializable id = idEntity.getId();
        Assert.notNull(id, "id 不能为null");
        if (id instanceof String) {
            Assert.notBlank((String) id, "id 不能为空");
        }

        UpdateWrapper<EDB> updateWrapper = new UpdateWrapper<EDB>()
            .eq(idEntity.getIdColumnName(), idEntity.getId());

        EDB edb = V2DbEntityConvert.convertToDbEntity(entity);
        // updateByWrapper不能走填充策略，只有entity才能填充
        // 参考连接 https://www.shangmayuan.com/a/3a4189a6a4424846aa96eb61.html

        return super.update(edb, updateWrapper);
    }

    public boolean updateByWrapper(E entity, Wrapper wrapper) {
        EDB edb = V2DbEntityConvert.convertToDbEntity(entity);
        boolean b = this.update(edb, wrapper);
        if (entity instanceof IV2VersionEntity) {
            IV2VersionEntity versionEntity = (IV2VersionEntity) entity;
            IV2VersionEntity versionDbEntity = (IV2VersionEntity) edb;
            versionEntity.setVersion(versionDbEntity.getVersion());
        }
        return b;
    }

    @Override
    public boolean updateByCriteria(E entity, IV2Criteria<E, C> criteria) {
        return this.updateByWrapper(entity, V2WrapperUtil.getUpdateWrapper(criteria));
    }

    // ****************************** 查 start ******************************

    @Override
    public E getEntityById(Serializable id) {
        Class<EDB> edbClass = currentModelClass();
        Serializable realId = id;
        TableInfo tableInfo = TableInfoHelper.getTableInfo(edbClass);
        if (tableInfo != null && Number.class.isAssignableFrom(tableInfo.getKeyType())) {
            try {
                realId = Long.parseLong(id.toString());
            } catch (NumberFormatException e) {
                log.error(e.getMessage(), e);
                return null;
            }
        }

        if (IV2IdEntity.class.isAssignableFrom(edbClass)) {
            String IdColumnName = getIdColumnName(edbClass);
            if (StrUtil.isNotBlank(IdColumnName)) {
                return getByWrapper(new QueryWrapper<EDB>().eq(IdColumnName, realId));
            }
        }
        EDB edb = super.getById(realId);
        return edb == null ? null : edb.convertToEntity();
    }

    @Override
    public E getByIdOrException(Serializable id) {
        E t = this.getEntityById(id);
        if (t == null) {
            throw new AbsentException(id.toString());
        }
        return t;
    }

    public E getByWrapper(Wrapper<EDB> wrapper) {
        IPage<E> page = findPageByWrapper(new Page<E>(1, 1).setSearchCount(false), wrapper);
        return page.getRecords().isEmpty() ? null : page.getRecords().get(0);
    }

    @Override
    public E getByCriteria(IV2Criteria<E, C> criteria) {
        return getByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    public E getOrWarnByWrapper(Wrapper<EDB> wrapper) {
        E t = getByWrapper(wrapper);
        if (t == null) {
            log.warn(
                "{}: {}: {}",
                StatusMessageEnum.ABSENT.getMsg(),
                currentModelClass().getSimpleName(),
                wrapper.getSqlSegment());
        }
        return t;
    }

    @Override
    public E getByCriteriaOrWarn(IV2Criteria<E, C> criteria) {
        return getOrWarnByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    public E getByWrapperOrException(Wrapper<EDB> wrapper) {
        E entity = getByWrapper(wrapper);
        if (entity == null) {
            String values = wrapper == null
                ? ""
                : CollUtil.join(((QueryWrapper<EDB>) wrapper).getParamNameValuePairs().values(), ",");
            throw new AbsentException(values);
        }
        return entity;
    }

    @Override
    public E getByCriteriaOrException(IV2Criteria<E, C> criteria) {
        return getByWrapperOrException(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    public List<E> findByWrapper(Wrapper<EDB> wrapper) {
        // 如果没有设置 select 条件，则过滤掉敏感字段
        if (wrapper != null
            && !Optional.ofNullable(wrapper.getSqlSelect()).isPresent()
            && wrapper instanceof QueryWrapper) {
            ((QueryWrapper<EDB>) wrapper).select(
                currentModelClass(),
                i -> !SensitiveDataProperty.PROPERTIES.contains(i.getProperty()));
        }
        return this.list(wrapper).stream().map(V2DbEntityConvert::convertToEntity).collect(Collectors.toList());
    }

    @Override
    public List<E> findByCriteria(IV2Criteria<E, C> criteria) {
        return this.findByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    public IPage<E> findPageByWrapper(long pageNum, long pageSize, boolean searchCountFlag, Wrapper<EDB> wrapper) {
        return this.findPageByWrapper(new Page<>(pageNum, pageSize, searchCountFlag), wrapper);
    }

    public IPage<E> findPageByWrapper(IPage<E> page, Wrapper<EDB> wrapper) {
        if (wrapper != null
            && !Optional.ofNullable(wrapper.getSqlSelect()).isPresent()
            && wrapper instanceof QueryWrapper) {
            ((QueryWrapper<EDB>) wrapper).select(
                currentModelClass(),
                i -> !SensitiveDataProperty.PROPERTIES.contains(i.getProperty()));
        }
        return this.page(
            new Page<EDB>(page.getCurrent(), page.getSize(), page.isSearchCount())
                .setOptimizeCountSql(page.optimizeCountSql()),
            wrapper)
            .convert(V2DbEntityConvert::convertToEntity);
    }

    @Override
    public IPage<E> findPageByCriteria(IV2Criteria<E, C> criteria) {
        IV2PageCriteria pageCriteria = (IV2PageCriteria) criteria;
        return findPageByWrapper(
            pageCriteria.getPageNum(),
            pageCriteria.getPageSize(),
            pageCriteria.getSearchCountFlag(),
            V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    @Override
    public List<E> findAll() {
        return this.findByWrapper(null);
    }

    public Integer countByWrapper(Wrapper<EDB> wrapper) {
        return this.count(wrapper);
    }

    @Override
    public Integer countByCriteria(IV2Criteria<E, C> criteria) {
        return this.countByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    @Override
    public Integer countAll() {
        return this.count(null);
    }

    @Override
    public boolean isPresentById(Serializable id) {
        Assert.notNull(id, "id");
        if (id instanceof String) {
            Assert.notBlank((String) id, "id");
        }

        return isPresentByWrapper(Wrappers.<EDB>query().eq(IV2IdEntity.ID_COLUMN_NAME, id).last("limit 1"));
    }

    @Override
    public boolean isPresentByColumn(String columnName, Object value) {
        Assert.notBlank(columnName);
        Assert.notNull(value, "value");
        return isPresentByWrapper(Wrappers.<EDB>query().eq(columnName, value));
    }

    public boolean isPresentByWrapper(Wrapper<EDB> wrapper) {
        return this.countByWrapper(wrapper) > 0;
    }

    @Override
    public boolean isPresentByCriteria(IV2Criteria<E, C> criteria) {
        return this.isPresentByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    public void exceptionIfPresentByWrapper(Wrapper<EDB> wrapper) {
        if (isPresentByWrapper(wrapper)) {
            String values = wrapper == null
                ? ""
                : CollUtil.join(((QueryWrapper<EDB>) wrapper).getParamNameValuePairs().values(), ",");
            throw new AlreadyPresentException(values);
        }
    }

    @Override
    public void exceptionIfPresentByCriteria(IV2Criteria<E, C> criteria) {
        exceptionIfPresentByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

    public void exceptionIfAbsentByWrapper(Wrapper<EDB> wrapper) {
        if (!isPresentByWrapper(wrapper)) {
            String values = wrapper == null
                ? ""
                : CollUtil.join(((QueryWrapper<EDB>) wrapper).getParamNameValuePairs().values(), ",");
            throw new AbsentException(values);
        }
    }

    @Override
    public void exceptionIfAbsentByCriteria(IV2Criteria<E, C> criteria) {
        exceptionIfAbsentByWrapper(V2WrapperUtil.getEdbQueryWrapper(criteria));
    }

}
