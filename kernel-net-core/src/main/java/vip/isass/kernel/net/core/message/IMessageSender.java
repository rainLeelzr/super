package vip.isass.kernel.net.core.message;

import lombok.Getter;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.Primary;
import org.springframework.core.Ordered;
import vip.isass.core.structure.service.V2ServiceManagerUtil;
import vip.isass.core.support.api.ApiOrder;
import vip.isass.kernel.net.core.tag.ITagService;
import vip.isass.kernel.net.core.tag.TagPair;

import java.util.Collection;
import java.util.Collections;
import java.util.List;
import java.util.stream.Collectors;

/**
 * 消息发送器，负责消息的发送，上层应用使用此类发送消息
 * 如果上层应用能直接拿到 session 对象即拥有 MessageSenderImpl，应直接通过 session 对象消息发送
 *
 * @author Rain
 */
public interface IMessageSender extends Ordered {

    Logger log = LoggerFactory.getLogger(IMessageSender.class);

    /*
    各实现类的 order:
    SocketIoSenderManager     Integer.MIN_VALUE
    RealSessionMessageSender  1
    RedisMessagePublisher     10
     */

    String SOCKETIO_URI_FIRST_PART = "/socketio-service/sender";

    String SEND_URL_SECOND_PART = "/send";
    String SOCKETIO_SEND_URL = SOCKETIO_URI_FIRST_PART + SEND_URL_SECOND_PART;
    String SOCKETIO_SEND_CMD = "POST" + " " + SOCKETIO_SEND_URL;

    /**
     * 广播消息
     *
     * @param payload 消息内容
     */
    default void broadcastMessage(Object payload) {
        sendMessageByTagPairs(null, payload, null);
    }

    /**
     * 广播消息
     *
     * @param cmd     路由命令
     * @param payload 消息内容
     */
    default void broadcastMessage(String cmd, Object payload) {
        sendMessageByTagPairs(cmd, payload, null);
    }

    /**
     * 发送消息给指定的用户
     *
     * @param payload 消息内容
     * @param userId  用户 id
     */
    default void sendMessageByUserId(Object payload, String userId) {
        sendMessageByTagPairs(
            null,
            payload,
            Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY, userId)));
    }

    /**
     * 发送消息给指定的用户
     *
     * @param cmd     路由命令
     * @param payload 消息内容
     * @param userId  用户 id
     */
    default void sendMessageByUserId(String cmd, Object payload, String userId) {
        sendMessageByTagPairs(
            cmd,
            payload,
            Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY, userId)));
    }

    /**
     * 发送多条消息给指定的用户
     *
     * @param payloads 消息内容集合
     * @param userId   用户 id
     */
    default void sendMessagesByUserId(Collection<Object> payloads, String userId) {
        for (Object payload : payloads) {
            sendMessageByTagPairs(
                null,
                payload,
                Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY, userId)));
        }
    }

    /**
     * 发送消息给指定的用户集合
     *
     * @param payload 消息内容
     * @param userIds 用户 id 集合
     */
    default void sendMessageByUserIds(Object payload, Collection<String> userIds) {
        for (String userId : userIds) {
            sendMessageByTagPairs(
                null,
                payload,
                Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY, userId)));
        }
    }

    /**
     * 发送消息给指定的用户集合
     *
     * @param cmd     路由命令
     * @param payload 消息内容
     * @param userIds 用户 id 集合
     */
    default void sendMessageByUserIds(String cmd, Object payload, Collection<String> userIds) {
        for (String userId : userIds) {
            sendMessageByTagPairs(
                null,
                payload,
                Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY, userId)));
        }
    }

    /**
     * 发送多条消息给指定的用户集合
     *
     * @param payloads 消息内容集合
     * @param userIds  用户 id 集合
     */
    default void sendMessagesByUserIds(Collection<Object> payloads, Collection<String> userIds) {
        for (String userId : userIds) {
            for (Object payload : payloads) {
                sendMessageByTagPairs(
                    null,
                    payload,
                    Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY, userId)));
            }
        }
    }

    /**
     * 发送消息给已登录用户
     *
     * @param payload 消息内容
     */
    default void sendMessageToLoginUsers(Object payload) {
        sendMessageByTagPairs(
            null,
            payload,
            Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY)));
    }

    /**
     * 发送消息给已登录用户
     *
     * @param cmd     路由命令
     * @param payload 消息内容
     */
    default void sendMessageToLoginUsers(String cmd, Object payload) {
        sendMessageByTagPairs(
            null,
            payload,
            Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY)));
    }

    /**
     * 发送消息给已登录用户
     *
     * @param payloads 消息内容集合
     */
    default void sendMessagesToLoginUsers(Collection<Object> payloads) {
        for (Object payload : payloads) {
            sendMessageByTagPairs(
                null,
                payload,
                Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY)));
        }
    }

    /**
     * 发送消息给拥有指定标签键的用户
     *
     * @param payload 消息内容
     * @param tagKey  标签键
     */
    default void sendMessageByTagKey(Object payload, String tagKey) {
        sendMessageByTagPairs(
            null,
            payload,
            Collections.singleton(new TagPair(ITagService.USER_ID_TAG_KEY)));
    }

    /**
     * 发送消息给拥有指定标签键集合的用户
     *
     * @param payload 消息内容
     * @param tagKeys 标签键集合
     */
    default void sendMessageByTagKeys(Object payload, Collection<String> tagKeys) {
        sendMessageByTagPairs(
            null,
            payload,
            tagKeys.stream()
                .map(TagPair::new)
                .collect(Collectors.toList()));
    }

    /**
     * 发送消息给拥有指定标签键值对的用户
     *
     * @param payload  消息内容
     * @param tagKey   标签键
     * @param tagValue 标签值
     */
    default void sendMessageByTagPair(Object payload, String tagKey, String tagValue) {
        sendMessageByTagPairs(
            null,
            payload,
            Collections.singleton(new TagPair(tagKey, tagValue)));
    }

    /**
     * 发送消息给拥有指定标签键值对的用户
     *
     * @param cmd      路由命令
     * @param payload  消息内容
     * @param tagKey   标签键
     * @param tagValue 标签值
     */
    default void sendMessageByTagPair(String cmd, Object payload, String tagKey, String tagValue) {
        sendMessageByTagPairs(
            cmd,
            payload,
            Collections.singleton(new TagPair(tagKey, tagValue)));
    }

    /**
     * 发送消息给拥有指定标签键值对集合的用户
     *
     * @param cmd     路由命令
     * @param payload 消息内容
     * @param tags    标签键值对集合
     */
    default void sendMessageByTagPairs(String cmd, Object payload, Collection<TagPair> tags) {
        sendMessage(Message.builder()
            .cmd(cmd)
            .payload(payload)
            .tags(tags)
            .build());
    }

    /**
     * 发送消息给客户端
     *
     * @param message 消息
     */
    void sendMessage(Message message);

    /**
     * 获取发送器的类型
     *
     * @return 发送器类型
     */
    SenderType getSenderType();

    /**
     * @author rain
     */
    @Primary
    @Configuration
    class MessageSenderManager implements IMessageSender {

        @Getter
        @Autowired(required = false)
        private List<IMessageSender> services;

        @Override
        public void sendMessage(Message message) {
            log.debug("发送消息:{}}", message);
            V2ServiceManagerUtil.consume(services, s -> s.sendMessage(message));
        }

        @Override
        public SenderType getSenderType() {
            return null;
        }

        @Override
        public int getOrder() {
            return ApiOrder.SERVER_MANAGER;
        }
    }

}
